public class operators {
    public static void main(String[] args) {
        int num1=60;
        int num2=20;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2);
    }
    
}